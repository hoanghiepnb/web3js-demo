const auctionContract = new web3.eth.Contract(auctionABI, auctionAddress);


async function getEventFromBlock(fromBlock, toBlock) {
    try {
        const events = await auctionContract.getPastEvents('AuctionCreated', {
            fromBlock,
            toBlock
        });
        // console.log(events);
        for (let i = 0; i < events.length; i++) {
            await parseEvent(events[i]);
        }
    } catch (e) {
        console.log(e);
    }
}

async function parseEvent(log) {
    const eventAbi = auctionABI.find(e => e.name === 'AuctionCreated');
    const decoded = web3.eth.abi.decodeLog(eventAbi.inputs, log.data, log.topics.slice(1));
    console.log(decoded.nftAddress, decoded.nftId, decoded.paymentToken, decoded.minPrice);
    // console.log(decoded);
}


